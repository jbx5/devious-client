	public BufferRenameTransformer(ParallelExecutorMapping mapping, ClassGroup namedGroup)
	{
		this.mapping = mapping;
		this.namedGroup = namedGroup;
	}

	@Override
	public void transform(ClassGroup targetGroup)
	{
		Stopwatch stopWatch = Stopwatch.createStarted();

		ClassFile namedBufferCF = namedGroup.findClass("Buffer");
		ClassFile targetBufferCF = null;

		// Find buffer class
		outer:
		for (ClassFile classFile : targetGroup)
		{
			Method clinit = classFile.findMethod("<clinit>", new Signature("()V"));
			if (clinit == null) continue;
			if (clinit.getCode() == null) continue;
			if (clinit.getCode().getInstructions() == null) continue;
			for (Instruction insn : clinit.getCode().getInstructions().getInstructions())
			{
				if (insn instanceof LDC)
				{
					if (((LDC) insn).getConstant() instanceof Long && ((LDC) insn).getConstantAsLong() == -3932672073523589310L)
					{
						targetBufferCF = classFile;
						break outer;
					}
				}
			}
		}

		List<Method> namedMethods = namedBufferCF.getMethods().stream()
			.filter(m -> !m.isStatic())
			.filter(m -> !m.getName().equals("<clinit>"))
			.filter(m -> !m.getName().equals("<init>"))
			//.filter(m -> m.getCode() != null)
			.collect(Collectors.toList());

		List<Method> targetMethods = targetBufferCF.getMethods().stream()
			.filter(m -> !m.isStatic())
			.filter(m -> !m.getName().equals("<clinit>"))
			.filter(m -> !m.getName().equals("<init>"))
			//.filter(m -> m.getCode() != null)
			.collect(Collectors.toList());

		// Size mismatch
		if (targetMethods.size() != namedMethods.size())
		{
			logger.warn("targetMethodSize: {} != namedMethodSize: {}", targetMethods.size(), namedMethods.size());
			//return;
		}

		// Named methods matches hard coded methods?
		int namedMethodsCount = (int) namedMethods.stream().filter(m -> !m.getName().startsWith("method")).count();
		int targetMethodsCountHardcoded = TargetBufferMethods.TARGET_BUFFER_METHODS.keySet().size();
		if (namedMethodsCount != targetMethodsCountHardcoded)
		{
			List<String> newMappedMethods = namedMethods.stream()
				.filter(m -> !m.getName().startsWith("method") && !TargetBufferMethods.TARGET_BUFFER_METHODS.containsKey(m.getName()))
				.map(Method::getName)
				.collect(Collectors.toList());

			logger.error("Add the newly mapped methods: {}", newMappedMethods.toString());
		}

		// Print
		//printBufferMethods(namedMethods, targetMethods);
		//System.exit(0);

		// Remove existing buffer mapping
		//this.mapping.getMap().keySet().removeIf(k -> k.toString().startsWith("Buffer.") || k.toString().contains(" Buffer."));
		this.mapping.getMapInstance().keySet().removeIf(k -> k.toString().startsWith("Buffer.") || k.toString().contains(" Buffer."));

		// Map class
		mapping.map(null, namedBufferCF, targetBufferCF);

		// Map fields
		List<Field> namedFields = namedBufferCF.getFields();
		List<Field> targetFields = targetBufferCF.getFields();
		for (int i = 0; i < namedFields.size(); i++)
		{
			Field namedField = namedFields.get(i);
			Field targetField = targetFields.get(i);
			if (namedField.getName().startsWith("field"))
			{
				continue;
			}
			logger.info("Mapped field: {} -> {}", targetField.getName(), namedField.getName());
			mapping.map(null, namedField, targetField);
		}

		// Map methods
		Map<String, Method> namedMethodsM = namedMethods.stream()
			.collect(Collectors.toMap(Method::getName, Function.identity()));
		for (Method targetMethod : targetMethods)
		{
			List<String> targetIns = targetMethod.getCode().getInstructions().getInstructions().stream()
				.map(this::getInstructionAsString)
				.collect(Collectors.toList());

			for (Map.Entry<String, TargetBufferMethods> entry : TargetBufferMethods.TARGET_BUFFER_METHODS.entrySet())
			{
				String name = entry.getKey();
				List<List<String>> comparingIns = entry.getValue().getInstructions();

				if (comparingIns.stream().anyMatch(targetIns::equals))
				{
					Method namedMethod = namedMethodsM.get(name);
					if (namedMethod != null)
					{
						mapping.map(null, namedMethod, targetMethod);
						logger.info("Mapped method: {} -> {}", targetMethod.getName(), name);
					}
					else
					{
						logger.error("Named method: {} is not present!", name);
					}
					break;
				}
			}
		}

		namedMethods.stream()
			.filter(m -> !m.getName().startsWith("method") && !mapping.getMap().containsKey(m))
			.forEach(m -> logger.error("Failed to map: {}", m.getName()));

		logger.warn("Buffer rename order might have been incorrect verify manually!");
		logger.info("Took: {}", stopWatch);
	}

	private void printBufferMethods(List<Method> namedMethods, List<Method> targetMethods)
	{
		if (namedMethods.size() != targetMethods.size())
		{
			return;
		}

		for (int i = 0; i < Math.max(namedMethods.size(), targetMethods.size()); i++)
		{
			Method namedMethod = namedMethods.get(i);
			if (namedMethod.getName().startsWith("method"))
			{
				continue;
			}

			Method targetMethod = targetMethods.get(i);
			List<String> targetIns = targetMethod.getCode().getInstructions().getInstructions().stream()
				.map(this::getInstructionAsString)
				.collect(Collectors.toList());

			System.out.println(namedMethod.getName());
			for (int j = 0; j < targetIns.size(); j++)
			{
				if (j < targetIns.size() - 1)
				{
					System.out.println('"' + targetIns.get(j) + '"' + ",");
				}
				else
				{
					System.out.println('"' + targetIns.get(j) + '"');
				}
			}

			TargetBufferMethods targetBufferMethod = TargetBufferMethods.TARGET_BUFFER_METHODS.get(namedMethod.getName());
			if (targetBufferMethod != null && TargetBufferMethods.TARGET_BUFFER_METHODS.get(namedMethod.getName()).getInstructions().stream().anyMatch(targetIns::equals))
			{
				System.out.println("Match: " + namedMethod.getName());
			}
			else
			{
				System.out.println("Ins does not match: " + namedMethod.getName());
			}

			System.out.println("\n");
		}
	}

	private String getInstructionAsString(Instruction in)
	{
		if (in instanceof PushConstantInstruction)
		{
			return in.toString();
		}
		else if (in instanceof LVTInstruction)
		{
			return in.getType() + " " + ((LVTInstruction) in).getVariableIndex();
		}
		return in.getType().toString();
	}

